static const uint8_t FONT_WIDTH = 8;
static const uint8_t FONT_HEIGHT = 8;
static const uint16_t DISPLAY_WIDTH = EInkDisplay::DISPLAY_WIDTH;
static const uint16_t DISPLAY_HEIGHT = EInkDisplay::DISPLAY_HEIGHT;

static void drawChar(EInkDisplay& display, char c, int16_t x, int16_t y) {
    if (c < 32 || c > 126) {
        c = ' ';
    }

    uint8_t* fb = display.getFrameBuffer();
    const uint8_t* charData = font8x8[c - 32];

    for (uint8_t row = 0; row < FONT_HEIGHT; row++) {
        for (uint8_t col = 0; col < FONT_WIDTH; col++) {
            int16_t px = x + col;
            int16_t py = y + row;

            if (px < 0 || px >= DISPLAY_WIDTH || py < 0 || py >= DISPLAY_HEIGHT) {
                continue;
            }

            uint16_t byteIndex = py * (DISPLAY_WIDTH / 8) + (px / 8);
            uint8_t bitIndex = 7 - (px % 8);

            if (charData[row] & (1 << (7 - col))) {
                fb[byteIndex] &= ~(1 << bitIndex);
            }
        }
    }
}

static void drawString(EInkDisplay& display, const char* str, int16_t x, int16_t y) {
    int16_t cursorX = x;
    while (*str) {
        drawChar(display, *str, cursorX, y);
        cursorX += FONT_WIDTH;
        str++;
    }
}

static void drawCenteredString(EInkDisplay& display, const char* str, int16_t y) {
    int16_t len = 0;
    const char* p = str;
    while (*p) {
        len++;
        p++;
    }

    int16_t stringWidth = len * FONT_WIDTH;
    int16_t x = (DISPLAY_WIDTH - stringWidth) / 2;

    drawString(display, str, x, y);
}

void showNoSdCard(EInkDisplay& display) {
    display.clearScreen(0xFF);

    const char* title = "ERROR";
    const char* message = "Insert SD Card";

    int16_t centerY = DISPLAY_HEIGHT / 2;
    drawCenteredString(display, title, centerY - 32);
    drawCenteredString(display, message, centerY + 8);

    display.displayBuffer(EInkDisplay::FULL_REFRESH);
}

void showNoConfig(EInkDisplay& display) {
    display.clearScreen(0xFF);

    const char* title = "ERROR";
    const char* message = "Config file missing";
    const char* path = "Expected: /config.json";

    int16_t centerY = DISPLAY_HEIGHT / 2;
    drawCenteredString(display, title, centerY - 48);
    drawCenteredString(display, message, centerY - 8);
    drawCenteredString(display, path, centerY + 24);

    display.displayBuffer(EInkDisplay::FULL_REFRESH);
}

void showWiFiError(EInkDisplay& display, const char* ssid) {
    display.clearScreen(0xFF);

    const char* title = "ERROR";
    char message[64];
    snprintf(message, sizeof(message), "WiFi failed: %s", ssid);

    int16_t centerY = DISPLAY_HEIGHT / 2;
    drawCenteredString(display, title, centerY - 24);
    drawCenteredString(display, message, centerY + 16);

    display.displayBuffer(EInkDisplay::FULL_REFRESH);
}

